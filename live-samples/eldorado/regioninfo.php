          <p>2 lbs  Small potatoes (new potatoes, Yukons, or fingerlings),  scrubbed but not peeled <br>
          1 tsp   Salt <br>
          1   Red onion, peeled, halved, and either thinly sliced or finely chopped<br>
          2   Garlic cloves, minced <br>
