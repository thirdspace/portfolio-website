                <h3>Braised Lamb a la Ali</h3>
              <p>Rhone wines from El Dorado are great food wines, packed with a
                broad spectrum of fruit and spice flavors and a food-friendly acidic 
                balance that harmonizes with a variety of cuisines. I came up with the 
                following recipe after being treated to the sensual Persian dishes our 
                friend Ali Esteghalian learned from his mother in Iran. The complexity 
                of the spices and fruits match the vibrant flavors of El Dorado Rhones 
                in particular.</p>
              <p>This lamb dish, and its chicken variation, is easy to make 
                ahead, and everyone, regardless of whether they know a Mourvedre from a 
                Marsanne, goes back for seconds. There's no marinating overnight, and 
                the dish uses just one pot. The secret is to let it cook slowly, and let
                the flavors gain integration and intensity much like the flavors in 
                wine over time. The best method I've found is to throw all of the 
                ingredients together, plop it in the oven, and then go out to a meeting 
                or a winetasting. When I come back, I just whip up some cous cous and a 
                salad, and dinner is served. A Sante,</p>
              <p>Elaine Smith, Wine Consultant</p>
              <p><strong>Ingredients: Feel free to improvise!</strong></p>
              <p>4-6 Lamb shanks, cracked by your butcher if possible. Approx. 4 lbs.<br>
                2 Tblsp olive oil<br>
                1 cup onion, chopped<br>
                1/2 cup shallot, minced (or just use two cups onion total)<br>
                10-12 cloves of fresh garlic, peeled, whole or smushed with the side of your knife<br>
                Spices: 1/2 Tsp Cardamon, 1Tsp Cinnamon, 1/2 Tsp Clove, 1 Tblsp Coriander, 2 Tsp Cumin, 1/2 Tsp White Pepper<br>
                4 cups tomatoes, quartered. Romas work great. Or use a 12oz can of chopped tomatoes with juice.<br>
                Zest and juice of two lemons, or 1 lemon and one orange. Save some curly zest for garnish.<br>
                16 prunes 1 cup dried cherries or mixed dried berries<br>
                1/2 cup roasted salted almonds, chopped or slivered.<br>
                4 cups to 1 bottle of dry white El Dorado wine, such as Madrona Marsanne<br>
                1/2 cup chopped flat leafed parsley. Save some nice sprigs for garnish.</p>
              <p><strong>Directions:</strong></p>
              <p>Preheat oven to 300 F. Season lamb shanks with salt and pepper to taste.</p>
              <p>In a large oven-proof casserole, braiser, or skillet with 
                tight fitting lid, brown lamb shanks over medium high heat in olive oil,
                working in two batches if necessary. When both sides are browned, 
                remove shanks to a dinner plate or glass bowl.</p>
              <p>Add onions, shallots and garlic in remaining olive oil and 
                saute in lamb drippings until almost translucent, about 15 minutes. Stir
                occasionally. Meanwhile, chop tomatoes and zest the citrus.</p>
              <p>Add spices, and saute until they become fragrantly toasty, but
                never burnt, about 6 minutes. Meanwhile, chop almonds and parsley.</p>
              <p>Add tomatoes, citrus zest and juice, prunes, dried cherries, 
                raisins, and almonds to the pan. Saute until tomatoes begin to release 
                juice. Add wine and bring to a simmer. Add parsley.</p>
              <p>Carefully add lamb shanks, easing each one so that they are 
                tucked in among the fruit and the liquid. Each should be submerged 
                halfway or more.</p>
              <p>Bring back to a simmer, cover tightly, and place in oven.</p>
              <p>Pour yourself a glass of El Dorado Rhone varietal wine, such 
                as the Perry Creek El Dorado Viognier, and go about your business for 
                about three hours. A heavenly smell will take over your home. Come back 
                in the kitchen and check on your creation. The meat should be falling 
                from the bones.</p>
              <p>Serve over cous cous* whenever you are ready. This dish keeps well in the refrigerator, made a day or two ahead.</p>
              <p>*Make cous cous by pouring boiling water over an equal part 
                dry cous cous. Add a dash of salt, cover and let soften for 5-10 
                minutes. Fluff with a fork, and taste. Add a little more boiling water 
                if it seems too stiff, and cover until time to serve.</p>
              <p><strong>Wine Pairing:</strong></p>
              <p>I served this lamb dish to rave reviews recently with three red Rhones from El Dorado.</p>
              <p>Fitzpatrick Grenache El Dorado<br>
                Granite Springs Syrah El Dorado<br>
                Sierra Vista Syrah El Dorado</p>
