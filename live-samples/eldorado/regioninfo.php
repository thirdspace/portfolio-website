            <div class="recipe-content col-md-12">
               <h3>Parchment-wrapped Salmon with Ginger, Fennel &amp; Viognier-Roussanne</h3>
              <p><em>Compliments of Narrow Gate Winery<br>
                </em>Pair with: Narrow Gate Viognier, Roussanne or Viognier-Roussanne blend, or non-malolactic fermented Chardonnay</p>
              <p>&nbsp;<strong>Ingredients:</strong></p>
              <p>4&nbsp;4 oz. fresh salmon fillets (skinless/boneles<br>
                4&nbsp;8&#8221; &#8211; 10&#8221; square sheets of parchment paper (available on the plastic wrap &amp; foil aisle)<br>
                3 T Extra virgin olive oil<br>
                1&nbsp;Medium fresh fennel bulb, fronds removed, thinly sliced<br>
                1&nbsp;Medium sweet, white onion, cut in half, sliced thin<br>
                1 ea. Red, Yellow &amp; Orange bell pepper, sliced thin<br>
                2 T&nbsp;peeled, grated, fresh ginger (do not use jarred ginger)<br>
                1 t&nbsp;fennel or anise seeds<br>
                1 t&nbsp;honey<br>
                1 cup white wine (Narrow Gate Viognier-Roussanne) for deglazing the pan<br>
                Sea Salt &amp; fresh ground pepper to taste</p>
              <p><strong>Directions:</strong></p>
              <p>In a stainless steel bottom saut&eacute; pan (do not use&nbsp; a 
                &#8220;non-stick&#8221; pan) set on medium high heat, add olive oil, heat olive oil 
                and add fennel, onion, bell peppers, fennel or anise seed &amp; cook 
                through about 15 minutes, until light brown bits begin to form and stick
                to the bottom of the pan. When all begins to soften &amp; brown add 1 
                cup Viognier Roussanne (or other aromatic white wine) and scrape up 
                browned bits on bottom of pan, cook until wine is reduced and almost 
                fully cooked off.&nbsp; Remove from heat and stir in fresh ginger &amp; 
                honey, sprinkle with sea salt &amp; fresh ground pepper to taste.</p>
              <p>Lay salmon fillet in middle of parchment paper, and sprinkle lightly with sea salt.</p>
              <p>Top each fillet with 1/3 to 1 cup of vegetables.&nbsp; Pull up 
                opposite sides of parchment until the sides touch evenly; make a double 
                1&#8221; fold so the salmon is encapsulated in the parchment.&nbsp; Twist both
                open-ended sides of parchment tight, so it looks like a giant candy 
                wrapper.&nbsp; Place parchment wrapped salmon on a cookie sheet and 
                either bake in a pre-heated 400-degree oven for 15 minutes or in a 
                barbeque, setting the baking pan right on top of the grate.</p>
              <p>Make Ahead:&nbsp; Saut&eacute;ed topping can be made 2 days ahead and 
                stored in an airtight container.&nbsp;&nbsp; Fully assembled, parchment 
                wrapped fish can be made 24 hours in advance &amp; removed from 
                refrigeration 30 minutes before cooking</p>
              <p>Variations: Any good quality fish can be used.&nbsp; Also, you can add shrimp w/shell, whole<br>
                clams &amp; mussels.&nbsp; Make appetizer size using smaller pieces.</p>
              <p>Note: This is amazingly tasty and simple to prepare because the 
                salmon actually poaches in the parchment and stays moist. Serves 4 as 
                main course; more as appetizer.</p>
