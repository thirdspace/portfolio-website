          <h1 class="spacer-top-40 block">Region Facts</h1>

          <ul class="two-col photo-bg">
            <li>Total Crop Value (2013): $8,010,833</li>
            <li>Wine grape acreage (2013): approx. 2,343 acres</li>
            <li>Total wine grape acreage, as well as the value of the county's grapes, more than doubled in the last 10-15 years.</li>
            <li>Average price per ton of wine grapes sold in 2013: $1,449.</li>
            <li>Amount of wine grapes grown (2013): 5,740 tons</li>
            <li>Total wine grape tonnage sold outside of El Dorado County (2013): Nearly 2,556 tons</li>
            <li>Average yield per acre (2013): nearly 2.6 tons</li>
            <li>Number of grape varieties grown: 25 major varieties, in addition to nearly 30 other, lesser-known varieties</li>
            <li>Top 5 grape varieties grown in El Dorado (in order, based on total tons produced): Cabernet Sauvignon, Zinfandel, Merlot, Syrah, Petite Sirah. Barbera and Chardonnay also are predominant.</li>
            <li>In the El Dorado Winery Association (41 family-owned wineries, representing El Dorado's major vintners), the largest growers are: Boeger Winery, Madroña Vineyards and Lava Cap Winery.</li>
            <li>Elevations at which premium wine grapes are grown in the El Dorado Appellation: 1,200 to 3,500 feet above sea level.</li>
            <li>El Dorado Appellation established: 1983</li>
            <li>Number of bonded wineries in El Dorado (as of June 2010): More than 60 and growing – ranking El Dorado one of the largest regions in the state. While several of these wineries do not have public tasting rooms, this growth represents significant interest in the El Dorado wine market.</li>
            <li>Agriculture's impact on El Dorado's economy: $383 million (estimated, 2009)</li>
            <li>El Dorado wine industry's economic impact: $175 million (estimated, 2009)</li>
            <li>The number of wineries in El Dorado more than doubled in the last 10 years.</li>
            <li>In 1870, El Dorado County was the third largest wine producer in the state, behind Los Angeles and Sonoma Counties.* In the late 1800s there were approximately 40 commercial wineries operating in the county.* The industry plummeted during Prohibition.</li>
          </ul>
