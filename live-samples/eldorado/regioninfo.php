               <h3><b>Eggplant Caponata</b></h3>
              <p>From the kitchen of Tara De La Rosa, Boeger Winery</p>
              <p>1 large eggplant, cubed<br>
                2 T. extra virgin olive oil<br>
                1 small red onion, sliced<br>
                3 cloves garlic, chopped<br>
                1 T. capers, drained<br>
                1/4 c. Boeger red wine, such as Primitivo<br>
                1 c. Italian tomato sauce<br>
                1/4 c. kalamata olives, chopped<br>
                1/4 c. fresh basil, chopped<br>
                1 baguette, thinly sliced &amp; toasted</p>
              <p>Quarter eggplant lengthwise and scoop out very seedy parts and
                discard.&nbsp; Cut eggplant into cubes and place in colander.&nbsp; 
                Sprinkle with salt and lightly toss until all pieces are evenly 
                coated.&nbsp; Set colander in sink and top with a sturdy, weighted plate
                and let drain for one hour.&nbsp; Rinse the eggplant cubes of excess 
                salt and pat dry.&nbsp; Preheat oven to 375 degrees.&nbsp; Spread the 
                eggplant on a greased cooking sheet and bake for 35 minutes, or until 
                nice and brown.</p>
              <p>In a large skillet add the 2 T. of oil.&nbsp; Add onion and 
                cook over moderate heat, until tender.&nbsp; Add garlic and cook for an 
                additional minute.&nbsp; Return eggplant to the skillet.&nbsp; Stir in 
                capers, olives, balsamic vinegar and red wine and cook until the liquid 
                has reduced to half.&nbsp; Add the tomato sauce and continue to cook 
                over low heat until thickened.&nbsp; Taste mixture, and then season with
                salt and pepper. Transfer to a bowl to cool.&nbsp; Spread the caponata 
                on baguette toasts and garnish with basil.</p>
              <p><strong>Serving suggestion:</strong><br>
                Boeger Primitivo</p>
