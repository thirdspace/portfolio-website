          <article id="recipe-009" class="row recipe recipe-009">
            <div class="recipe-content col-md-12">
              <h3><b>Memphis Style Ribs</b></h3>
              <p><em>Created by Patty Mastracco, Raley&#8217;s<br>
                </em>Pair with: El Dorado Zinfandel</p>
              <p><b><i>Ribs:</i></b></p>
              <p>2 tbsp. paprika<br>
                1 tbsp. onion powder<br>
                1-1/2 tsp. pepper<br>
                1-1/4 tsp. salt<br>
                1/2 tsp. cayenne pepper<br>
                1 rack pork ribs<br>
                Soaked wood chips</p>
              <p><i>Prep time: 10 minutes, Cook time: 2 to 3 hours</i></p>
              <p> <strong>Directions:</strong></p>
              <p>Stir together seasonings in a small bowl. Rub over the surface of
                ribs, putting most of the mixture on the top. Place soaked chips in a 
                wood chip basket and place directly on coals or on gas grill over high 
                heat. When chips begin to smoke, place ribs on grill over indirect heat.
                (Using indirect heat is important to not char the outside of the meat.)
                Cook for 1 to 2 hours, turning once or twice and adding more wood chips
                as necessary to maintain smoke. Remove from grill and place in a large 
                shallow pan. Add 1 inch of water to pan and seal well with heavy duty 
                foil. Return to grill and cook over very low heat or in 300°F oven for 1
                hour. Cut into individual ribs and serve with <i>Memphis Moppin' Sauce</i> (recipe below). <sub>­</sub>Makes 6 servings.</p>
              <p><b><i>Memphis Moppin' Sauce:</i></b></p>
              <p>1 cup ketchup<br>
                1/2 cup minced onion<br>
                1/3 cup red wine vinegar<br>
                3 tbsp. yellow mustard<br>
                3 tbsp. brown sugar<br>
